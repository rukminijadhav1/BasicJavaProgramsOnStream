package shehjadsirAssignment.WednesdayAssignments;

public class PersonDetails {
	private String name;
    private int age;
    private int budget;

    public PersonDetails(String name, int age, int budget) {
        this.name = name;
        this.age = age;
        this.budget = budget;
    }
    
    public int getBudget() {
        return budget;
    }

}
